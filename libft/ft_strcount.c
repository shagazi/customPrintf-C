#include "libft.h"

int		ft_strcount(const char *s, char c)
{
	int	i;
	int	k;

	k = 0;
	i = 0;
	if (!s)
		return (0);
	while (s[i] != '\0')
	{
		if (s[i] == c && s[i + 1] != c)
			k++;
		i++;
	}
	if (s[0] != '\0')
		k++;
	return (k);
}
