#include "libft.h"

int		ft_spacecheck(char *s)
{
	int k;

	k = ft_strlen(s) - 1;
	while ((s[k] == ' ' || s[k] == '\n' || s[k] == '\t') && (k != 0))
		k--;
	return (k);
}
